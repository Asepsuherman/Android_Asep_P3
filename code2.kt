fun main() {
	// Membuat variabel dengan value number.
    var count = 22
    // Ini akan mencetak output sesuai pesan didalamnya,
    // dan memanggil variabel count yang mempunyai value 22
    println("Umur asep $count")

    // Ini akan mengurangi 1 nilai dari value variabel count yang ada menjadi 21
    count--
    println("Sebelumnya umur Asep $count")
}