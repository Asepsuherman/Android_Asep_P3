// *** Membuat dan Menggunakan fungsi di kotlin
fun main() {
    // Membuat variabel dengan value number.
    var count = 22
    // Println ini akan mencetak output standar sesuai pesan didalamnya dan memanggil variabel $count yang mempunyai value 22
    println("Umur asep $count")
    // output: Umur asep 22

    // Ini akan mengurangi 1 nilai dari value variabel count yang ada menjadi 21
    count--
    println("Sebelumnya umur Asep $count")
    // output: Sebelumnya umur asep 21

}
